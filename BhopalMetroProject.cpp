int mindistance(int distance[], bool vis[])
{
    int minimum = INT_MAX, index;
    for (int k = 0; k <= 52; k++)
    {
        if (vis[k] == false && distance[k] <= minimum)
        {
            minimum = distance[k];
            index = k;
        }
    }
    return index;
}

void dijkstra(int graph[53][53], int srcStation, string stations[53], int distance[53])
{

    bool vis[53];
    for (int k = 0; k <= 52; k++)
    {
        distance[k] = INT_MAX;
        vis[k] = false;
    }

    distance[srcStation] = 0;

    for (int k = 0; k <= 52; k++)
    {
        int m = mindistance(distance, vis);
        vis[m] = true;
        for (int i = 0; i <= 52; i++)
        {
            if (!vis[i] && graph[m][i] && distance[m] != INT_MAX && distance[m] + graph[m][i] < distance[i])
                distance[i] = distance[m] + graph[m][i];
        }
    }
    cout << "Minimum Number of stations from  " << stations[srcStation] << " to every station" << endl;
    for (int k = 0; k <= 52; k++)
    {
        cout << "\nMinimum stations from " << stations[srcStation] << " to " << stations[k] << " are " << distance[k];
    }
}
